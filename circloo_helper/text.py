class Text(CustomObject):
    def __init__(self,
                 text: str,
                 obj: Object,
                 spacing: float | int = 1):
        """
        Convert a text string into circloO Objects. Allows use of \n, \r, and \t.
        :param text:    str to be converted.
        :param obj:     Object to be tiled into char shapes. Top-left corner of text starts at this object's centroid
        :param spacing: Space between each letter as a scaler to obj's width; default is 1.
        """
        super().__init__()
        self.text = text
        self.obj = obj
        self.spacing = spacing

    def build_objs(self):
        super().build_objs()

        width, height = dimensions(self.obj)

        prev_width = 0
        obj = self.obj
        for char in self.text:
            if char == '\n':
                # New line.
                obj = translate(obj, 0, height * 6)
                continue
            elif char == '\r':
                # Carriage Return (move to initial x-value)
                obj = translate(obj, centroid(self.obj)[0] - centroid(obj)[0])
                prev_width = 0
                continue
            elif char == '\t':
                # Tab.
                ### TODO: more sophisticated tabbing (should line up on each line)
                prev_width += 15
                continue

            try:
                pix_arr = _CHARACTERS[char.upper()]
            except KeyError:
                # Use default 'missing' character if not in dict.
                pix_arr = _CHARACTERS['missing']

            obj = translate(obj, prev_width * width)
            self._obj_cache.extend(Pixels(pix_arr, obj).build_objs())

            prev_width = len(pix_arr[0]) + self.spacing

            # Kerning adjustments
            if char.upper() == 'Q':
                prev_width -= 1

        return self._obj_cache

